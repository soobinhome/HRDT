export const SAMPLE_EMPLOYEES: CandidateInternal[] = ROWS.map((r, i) => {
  const [
    name,
    grade,
    gradeGroup,
    age,
    org,
    lang,
    math,
    m,
    disc,
    ds,
    mbti,
    strengths,
    job,
    avgEval,
    ebg,
    mgr,
    sprout,
    ground,
  ] = r;
  const metrics = M.reduce(
    (acc, k, idx) => {
      acc[k] = m[idx];
      return acc;
    },
    {} as Record<MetricKey, Metric>
  );
  return {
    id: `emp-${i + 1}`,
    name,
    grade,
    gradeGroup,
    age,
    orgName: org,
    lang,
    math,
    metrics,
    disc: disc as DiscType,
    discScores: { D: ds[0], I: ds[1], S: ds[2], C: ds[3] },
    mbti,
    strengths,
    job,
    avgEval: avgEval as EvalGrade,
    ebgPass: ebg as OX,
    managerClass: mgr,
    sproutClass: sprout,
    groundExp: ground,
  };
});

export function findEmployeeByName(name?: string) {
  if (!name) return undefined;
  const clean = name.split("(")[0].trim();
  return SAMPLE_EMPLOYEES.find((e) => e.name === clean);
}
